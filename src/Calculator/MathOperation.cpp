#include"MathOperation.hpp"

 int MathOperation::add(int a, int b) const {
    return a + b;
}

int MathOperation::substract(int a, int b) const {
    return a - b;
}

int MathOperation::multiply(int a, int b) const {
    return a * b;
}

double MathOperation::divide(double a, double b) const {
    return a / b;
}